    public static Form getLetter(String s) {
        s = s.toUpperCase()
        s = s.replace("0", "ZERO")
        s = s.replace("1", "ONE")
        s = s.replace("2", "TWO")
        s = s.replace("3", "THREE")
        s = s.replace("4", "FOUR")
        s = s.replace("5", "FIVE")
        s = s.replace("6", "SIX")
        s = s.replace("7", "SEVEN")
        s = s.replace("8", "EIGHT");
        s = s.replace("9", "NINE")
        s = s.replace(" ", "WHITESPACE")
        s = s.replace(":", "COLON")
        return Letters.class.getField(s).get(null) as Form
    }

    public static List<Form> getLetters(String text) {
